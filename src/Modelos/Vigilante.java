package Modelos;

import java.util.Date; //Para usar el tipo de variable Date es necesario importar

public class Vigilante {
    
    // Declaración de atributos
    private String cedula,
            nombre,
            apellido,
            direccion,
            telefono;
    private Date fechaNacimiento;
    public char estatus;
    
    // Constructor de la clase
    public Vigilante(String cedula, String nombre,
            String apellido, String direccion, String telefono,
            Date fechaNacimiento, char estatus) {
        this.cedula = cedula;
        this.nombre = nombre;
        this.apellido = apellido;
        this.direccion = direccion;
        this.telefono = telefono;
        this.fechaNacimiento = fechaNacimiento;
        this.estatus = estatus;
    }
    
    // Creación de los Metodos Getter y Setter
    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public Date getFechaNacimiento() {
        return fechaNacimiento;
    }

    public void setFechaNacimiento(Date fechaNacimiento) {
        this.fechaNacimiento = fechaNacimiento;
    }

    public char getEstatus() {
        return estatus;
    }

    public void setEstatus(char estatus) {
        this.estatus = estatus;
    }

}
